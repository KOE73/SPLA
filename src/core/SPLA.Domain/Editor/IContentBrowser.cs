using System.Collections.Generic;

namespace SPLA.Domain.Editor;

/// <summary>
/// Обзор иерархического пространства редактируемого содержимого — источник данных для дерева файлов
/// слева в Workspace-shell (docs/schema-editor-plan.md, Фаза 3).
///
/// Зачем отдельно от <see cref="IContentSource"/>: источник умеет читать/писать ОДНУ ссылку, но не
/// умеет ПЕРЕЧИСЛЯТЬ. Браузер закрывает ровно этот разрыв и ничего больше — он только обходит дерево
/// и выдаёт <see cref="ContentNode"/>, чьи <see cref="ContentNode.Ref"/> потом скармливаются источнику.
/// Разделение держит оба контракта простыми и независимыми.
///
/// Как и прочие контракты редактора, НЕ предполагает файловую систему: реализация может обходить
/// каталог на диске, дерево проекта или виртуальное пространство — shell разницы не видит.
/// </summary>
public interface IContentBrowser
{
    /// <summary>Подпись корня дерева (напр. имя проекта/папки). Чисто для отображения.</summary>
    string RootLabel { get; }

    /// <summary>
    /// Дочерние узлы. <paramref name="parentRef"/> == null — верхний уровень (корень);
    /// иначе — <see cref="ContentNode.Ref"/> ранее выданной ветки.
    /// Ленивость намеренная: дерево раскрывается по мере необходимости, целиком не материализуется.
    /// </summary>
    IReadOnlyList<ContentNode> GetChildren(string? parentRef);
}
